package com.company.View;

public class CRectangle {
    private int top, left, width, height;

    public CRectangle(int left, int top, int width, int height){
            this.left=left;
            this.top=top;
            this.width=width;
            this.height=height;
        }

    public int getTop() {

            return top;
    }
    public int getLeft() {

            return left;
    }
    public int getWidth() {

        return width;
    }
    public int getHeight() {

        return height;
    }
}
